package com.example.myapplication.helpClasses;

public class inquiryImages {



    private String Img1;
    private String Img2;
    private String Img3;
    private String Img4;


    public inquiryImages() {
    }


    public inquiryImages(String img1, String img2, String img3, String img4) {
        Img1 = img1;
        Img2 = img2;
        Img3 = img3;
        Img4 = img4;
    }


    public String getImg1() {
        return Img1;
    }

    public void setImg1(String img1) {
        Img1 = img1;
    }

    public String getImg2() {
        return Img2;
    }

    public void setImg2(String img2) {
        Img2 = img2;
    }

    public String getImg3() {
        return Img3;
    }

    public void setImg3(String img3) {
        Img3 = img3;
    }

    public String getImg4() {
        return Img4;
    }

    public void setImg4(String img4) {
        Img4 = img4;
    }
}
